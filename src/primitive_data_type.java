 /* 1B.WAP in java to print
        the values of various primitive data types*/

public class primitive_data_type {

	public static void main(String[] args) {
		
 byte a=10;
 short b=20;
 int c=8;
 long d=34564890;
 float e= 5.5f;
 double f= 1.3;
 boolean g=true;
    System.out.print(a+"\n"+b+"\n"
 +c+"\n"+d+"\n"+e+"\n"+f+"\n"+g);
	}

}
